_id_counter = 0


def _next_id(prefix: str) -> str:
    global _id_counter
    _id_counter += 1
    return f"{prefix}-{_id_counter}"


@dataclass
class Bar:
    symbol: str
    ts: float           # epoch seconds (bar close time)
    open: float
    high: float
    low: float
    close: float
    volume: float = 0.0

    @property
    def is_up(self) -> bool:
        return self.close >= self.open


@dataclass
class Tick:
    symbol: str
    ts: float
    price: float
    size: float = 0.0


@dataclass
class Intent:
    """A strategy's expressed desire, before risk/sizing is applied."""
    type: IntentType
    reason: str = ""
    qty: Optional[int] = None   # strategy hint; risk manager may override


@dataclass
class Order:
    strategy_id: str
    symbol: str
    side: OrderSide
    qty: int
    type: OrderType = OrderType.MARKET
    price: Optional[float] = None
    stop_price: Optional[float] = None
    status: OrderStatus = OrderStatus.PENDING
    id: str = field(default_factory=lambda: _next_id("ORD"))
    ts: float = field(default_factory=time.time)
    reason: str = ""


